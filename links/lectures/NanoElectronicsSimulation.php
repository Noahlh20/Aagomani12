              <h2 align="left">ABSTRACT :</h2>
<p>For decades Moore's Law and traditional scaling of electronics have brought ever increasing gains in performance, making the modern electronics age possible.  Arguably the most important part of this scaling is the decreasing size of the transistor. However, approximately 10 years ago, the industry was faced with a dilemma that could have derailed Moore's Law.   Due to the quantum nature of the electron, the transistor could not be scaled geometrically as it had been, in decades past.</p>
<p>&nbsp;&nbsp;&nbsp;In order to keep electronics performance increasing, scientists and engineers had to resort to clever and innovative techniques in redesigning the traditional transistor.  This has spurred a decade of innovation in the CMOS technology, gradually but surely transitioning the industry from classical microelectronics to modern nanoelectronics.  In this talk, I will summarize the exciting evolution of the transistor in the last decade, covering innovations in strain engineering, metal gates, and alternative device structures, including an insider's perspective on the development of the FinFET.  The talk will also include a view of the road ahead, with a critical look at graphene and carbon nanotube technologies.</p>
              
