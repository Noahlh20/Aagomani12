              <h2 align="left">SPEAKER BIO :</h2>
              <img src="images/Jakub.jpg" id="floatrightSpeaker" />
              <p><span class="blu"><b>Jakub Kedzierski</b></span> received his Ph.D. in electrical engineering from the University of California at Berkeley in 2001, where he was one of the co-inventors of the FinFET device architecture. After graduation, Jakub worked at IBM's T.J. Watson Research Center working on FinFETs and metal-gate transistors. In 2005, he joined MIT Lincoln Laboratory where he is the Assistant Group Leader in the Advanced Silicon Devices Group.  Currently he is on leave from MIT to serve as a visiting faculty at the Indian Institute of Technology Bombay. His research interests include: advanced silicon devices, low-power electronics, 3D integration, graphene, and microfluidics. Jakub has been awarded the IEEE EDS Paul Rappaport, and the IEEE EDS George Smith Award for best-papers, and has participated in IEDM's process committee, NSF review panel and contributed as a reviewer for the IEEE.  He has over 60 publications and patents in the electronics area.</p>

